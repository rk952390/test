// * * * * * *   here row is =3 and column =6
// * * * * * *
// * * * * * *

public class loop_pattern_12 {
    public static void main(String args[]){
    
        for(int i =1;i<=3;i++)
        {
            for(int j=1 ; j<=6; j++)
            {
                System.out.print(" * ");

            }
            System.out.println();
        }
    }
}
